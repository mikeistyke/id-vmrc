def set_cell_bg(cell, hex6):
    tcPr = cell._tc.get_or_add_tcPr()
    shd = OxmlElement('w:shd')
    shd.set(qn('w:fill'), hex6)
    shd.set(qn('w:val'), 'clear')
    tcPr.append(shd)

def set_table_borders(table, color='CCCCCC', sz=4):
    tbl = table._tbl
    tblPr = tbl.tblPr
    tblBorders = OxmlElement('w:tblBorders')
    for side in ('top', 'left', 'bottom', 'right', 'insideH', 'insideV'):
        b = OxmlElement(f'w:{side}')
        b.set(qn('w:val'), 'single')
        b.set(qn('w:sz'), str(sz))
        b.set(qn('w:space'), '0')
        b.set(qn('w:color'), color)
        tblBorders.append(b)
    tblPr.append(tblBorders)

def set_col_widths(table, widths_inches):
    """Set column widths via cell XML for reliability."""
    for row in table.rows:
        for i, cell in enumerate(row.cells):
            if i < len(widths_inches):
                tc = cell._tc
                tcPr = tc.get_or_add_tcPr()
                tcW = OxmlElement('w:tcW')
                twips = str(int(widths_inches[i] * 1440))
                tcW.set(qn('w:w'), twips)
                tcW.set(qn('w:type'), 'dxa')
                # Remove existing tcW if present
                for old in tcPr.findall(qn('w:tcW')):
                    tcPr.remove(old)
                tcPr.append(tcW)

