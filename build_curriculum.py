def heading(doc, text, level=1, color=NAVY, pt_before=12, pt_after=6):
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(pt_before)
    p.paragraph_format.space_after  = Pt(pt_after)
    # Apply heading style
    p.style = doc.styles[f'Heading {level}']
    if p.runs:
        run = p.runs[0]
    else:
        run = p.add_run(text)
    run.text = text
    run.font.color.rgb = color
    return p

